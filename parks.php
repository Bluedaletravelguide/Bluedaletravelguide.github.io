      <h2 class="h2">ASEAN Sculpture Garden</h2>

      <p>
        If you are visiting the National Monument (Tugu Negara), you should not miss the ASEAN Sculpture Garden. It is a beautiful garden along Parlimen Road, about 100 meters from the National Monument and very close to Lake Gardens, and a 15-minute walk from the LRT station in Masjid Jamek. The experience is more elevated if you know more about the meaning behind every sculpture, as the information on site is quite scarce. The garden was officially opened to commemorate the 20th anniversary of ASEAN in 1987.
      </p>

      <p>
        <b>Location: </b><br>
        <a href="https://goo.gl/maps/gRkTFSUyQb37aVnu7" target="blank">ASEAN Sculpture Garden, Persiaran Sultan Salahuddin, Taman Tasik Perdana, 50480 Kuala Lumpur, Federal Territory of Kuala Lumpur</a><br>
        <b>Phone:</b><br>
        +6016-333 7328
      </p>

    </div>

    <img src="img2/parks/asean sculture park.PNG" class="entryimage" alt="ASEAN Sculpture Garden">

