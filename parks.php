      <h2 class="h2">Desa Park City</h2>

      <p>
        This is where you’d be in heaven beyond all the chaos. The Desa Parkcity is a township where it’s planned so well, the moment you reach the vicinity you feel like you are being separated from the outside world. The park is the perfect place for you to have a picnic overlooking the scenic lake, or get your heart pumped by hitting the jogging track. Take a leisurely stroll and enjoy the great outdoors with its breathtaking views. The cherry on the cake is that the park is also one of the many pet-friendly places in KL.
      </p>

      <p>
        <b>Location: </b><br>
        <a href="https://goo.gl/maps/89SsdP3DUU5MsLTb9" target="blank">C-3-1 Plaza Arkadia, 3, Persiaran Residen, Desa Parkcity, 52200 Kuala Lumpur, Wilayah Persekutuan Kuala Lumpur</a><br>
        <b>Operating Hours: </b><br>
        Daily, 6 am -11 pm<br>
      </p>

    </div>

  </div>

  <hr>

