      <h2 class="h2">Taman Eko Rimba, Kuala Lumpur</h2>

      <p>
        The KL Forest Eco Park is only 3 kilometers away from the heart of the city. It offers a wide range of attractions - you can camp, hike, and even conduct some research there! The Instagram worthy canopy walk is the coolest part of the park! The 200 m canopy walk overlooks the forest and surrounding skyscrapers! The famous eco-tourism destination can be accessed in many ways! The easiest way is to drive. You can find private parking for around RM 10.00 in Jalan Raja Chulan which is just a stone’s throw from the park.
      </p>

      <p>
        <b>Location: </b><br>
        <a href="https://goo.gl/maps/KYBBbomchyxgZu6d9" target="blank">Kuala Lumpur, 50250 Kuala Lumpur, Federal Territory of Kuala Lumpur</a><br>
        <b>Operating Hours: </b><br>
        7.00 am - 6.00 pm<br>
        <b>Phone: </b><br>
        +603- 2020 1606
      </p>

    </div>

