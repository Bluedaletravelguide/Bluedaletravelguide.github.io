            <h2 class="h2">Ulu Chepor, Perak</h2>

            <p>
              Lata Ulu Chepor is just 10 kilometers from Ipoh City and is embraced by verdant tropical rainforest hills. The park is accessible via Route A1 to Chemor, near the Ipoh Utara Toll Plaza.
              This place is ideal for getting some fresh air, resting, and having a picnic on weekends. For guests who wish to stay for an extended period or participate in instructional and leisure events, facilities such as a campground, multi-purpose hall, praying hall, and bathroom are available. The park is well-kept and marked on the main lane. If you're feeling brave, there's even a downhill track path.
            </p>

