            <h2 class="h2">Sungai Lembing, Pahang</h2>

            <p>
              Malaysia is endowed with a plethora of rainforests, which give way to breathtaking waterfalls. Rainbow waterfall is situated 45 kilometers northwest of Kuantan, Peninsular Malaysia, in the town of Sungei Lembing. The sun meets the falling water at the base of the falls every morning between 9 and 10 a.m., making a stunning rainbow. People fly from all over the world to make the one-and-a-half-hour trip to the falls only to see nature's beauty.
              Rainbow Waterfall is a large waterfall hidden in the Sungai Lembing rainforest area, accessible by 4WD vehicle ride and hiking.
            </p>

