            <h2 class="h2">Seven Wells Waterfall, Kedah</h2>

            <p>
              The Seven Wells Waterfall, also known as Jalan Telaga Tujuh, is situated about a 20-minute walk from the famous Langkawi Sky Bridge and is one of the island's most stunning natural attractions.
              The waterfall cascades down Mount Mat Cincang, Langkawi's second-highest peak, and is a popular attraction for tourists and locals. The complete vertical fall is 91 metres high, but only a small section can be seen from the bottom.
              According to legend, this waterfall is home to fairies and the river has soothing properties. The waterfall looks magical because it is surrounded by nature, with rich flora and fauna.
            </p>

