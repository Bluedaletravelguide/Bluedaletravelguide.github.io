            <h2 class="h2">Penang Hill, Penang</h2>

            <p>
              Bukit Bendera, located 821 metres above Penang's capital, is known by islanders and is usually five degrees cooler than Georgetown. Since it is Penang's only remaining patch of tropical rainforest, the flora and fauna here have been conserved since 1960. Although it is less well-known compared to the rest, but due to its cooler climate, it is one of Penang's most popular tourist destinations.
