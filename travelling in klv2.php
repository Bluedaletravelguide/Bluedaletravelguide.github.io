                The KL Monorail is a simple yet well-connected train system which runs between Kuala Lumpur’s transport hub KL Sentral and Titiwangsa in the heart of KL. It acts as a bridge between Kuala Lumpur city centre and the inner areas of Kuala Lumpur. The KL Monorail starts operations at 6.00 am and ceases at 12.00 am.<br>
				Helpline: 03-2267 9888
            </p>

