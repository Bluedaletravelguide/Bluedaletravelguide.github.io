            <p>
                The RapidKL Bus is commonly seen at most train stations and usually acts as a transit-based mode of transportation for passengers to directly get to their preferred destinations. Each trip on the bus can cost up to RM5.00 per trip which can be paid by using exact change or through tapping your Touch n’ Go card upon entry and exit.<br>
