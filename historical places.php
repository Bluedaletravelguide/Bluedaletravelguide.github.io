            <h2 class="h2">Royal Museum</h2>

            <p>
              Would you like to see what it's like to live in a palace? The Royal Museum is perhaps the best location to see and experience the royal way of life.

              The Royal Museum opened its doors on 1 February 2013, is housing in the old National Palace, which served as the official residence of Malaysia's King and Queen. The former National Palace has been turned into a museum and is now open to the public.
            <br><br>
              Within this two-story structure, you will see the Balairung Seri (a room where the King addresses his subjects), the sleeping chamber, the royal office, the dining hall, and the nation's King's resting lounge.
            </p>

