          <h2 class="h2">Istana Negara</h2>

          <p>
            Malaysia's National Palace is known as Istana Negara. It was built in 1928 and is located along Jalan Istana. The 13-acre castle served as the official residence of Malaysia's Yang di-Pertuan Agong (King). It was replaced in June 2011 by a modern, lavish RM800 million palace near Jalan Duta in Kuala Lumpur.

The old Istana Negara is located along Jalan Syed Putra in Bukit Petaling, overlooking the Klang River. Since the grounds are not open to the public, visitors normally take photos at the Main Palace Entrance. It was originally a double-story mansion owned by local Chinese millionaire Chan Wing, and was known as The Big Home.

          </p>

