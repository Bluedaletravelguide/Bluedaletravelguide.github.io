            <h2 class="h2">Mossy Forest, Cameron Highlands</h2>

            <p>
              The mossy forest is a natural ecosystem that only grows at the higher elevations of the Cameron Highlands and other Malaysian mountain ranges. Low-level clouds in the sky, driven by winds, blanket the trees in persistent mist and moisture, providing a perfect biotope for moss, ferns, lichen, and orchids.
              This moist tropical evergreen forest also acts as a refuge for a diverse variety of montane species, including butterflies, rodents, frogs, birds, and mammals that are only present in this cold climate. 
            </p>

