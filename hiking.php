            <h2 class="h2">Penang National Park, Penang</h2>

            <p>
              This is Malaysia's smallest national park, at 23 square kilometres, but its beach-fringed forests are home to silvered leaf monkeys, flying lemurs, leopard cats, and a plethora of lizard, amphibian, and reptile animals. Activities such as jungle walks and boat rides to serene golden-sand beaches will comfortably fill a day.

There are two major trekking trails in the park: one that takes you down the coast, over headlands, to Monkey Beach, and another that takes you over the hills, through the jungle, to some of the more isolated beaches, where the Turtle Sanctuary is located.

              
            </p>

