            <h2 class="h2">Mount Pulai, Johor</h2>

            <p>
              Gunung Pulai is a 654-meter-tall mountain in Johor, Peninsular Malaysia. Due to its proximity and ease of entry, it is a popular hiking destination for Singaporeans and locals. The summit of Gunung Pulai is not open to the public as it is secured by the military and holds two communication towers. 
              Mount Pulai is a 14.3-kilometer moderately trafficked loop trail near Kulai, Johor, Malaysia, with a waterfall and a difficulty rating. The trail is also used for camping, cycling, running, and taking nature walks.
            </p>

