            <h2>Currency</h2>
            
            <p>
                Malaysia’s currency unit is divided into two. The term Ringgit Malaysia (RM) is used to refer to paper money. The denominations are RM1, RM5, RM10, RM 20, RM 50 and RM100. Whereas the coins are referred to as sen (cents). The denominations are 5 sen, 10 sen, 20 sen and 50 sen.
				<br><br>
				<b>Samples of Ringgit Malaysia Notes:</b><br>
				1. Current Version of Notes and Coins<br><a href="https://www.bnm.gov.my/current-banknote-series" target="blank">https://www.bnm.gov.my/</a> (Sample of Notes)<br>
				<a href="https://www.bnm.gov.my/current-coin-series" target="blank">https://www.bnm.gov.my/</a> (Sample of Coins)<br>
				2. Old Version of Notes and Coins<br><a href="https://www.bnm.gov.my/malaysian-banknotes-and-coins-past-series" target="blank">https://www.bnm.gov.my/</a>
